package io.rhiot.component.pi4j.i2c.driver;

/**
 * 
 */
public class LSM303Value {
    private int x;
    private int y;
    private int z;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public String toString() {
        return "[" + x + "," + y + "," + z + "]";
    }
}
